#define HIGH 65
#define LOW 26

#define DELAY_LEN 182
#define LED_COUNT 14

#define ARRAY_LEN DELAY_LEN + LED_COUNT*24

uint16_t BUF_DMA_S1  [ARRAY_LEN];
uint16_t BUF_DMA_S2  [ARRAY_LEN];
uint16_t BUF_DMA_S3  [ARRAY_LEN];
uint16_t BUF_DMA_S4  [ARRAY_LEN];

#define STRIP1_BUF BUF_DMA_S3
#define STRIP2_BUF BUF_DMA_S1
#define STRIP3_BUF BUF_DMA_S2
#define STRIP4_BUF BUF_DMA_S4

#define BitIsSet(reg, bit) ((reg & (1<<bit)) != 0)

#define get_r_val(x) ((uint8_t)((x & 0xFF0000) >> 16))
#define get_g_val(x) ((uint8_t)((x & 0xFF00) >> 8))
#define get_b_val(x) ((uint8_t)((x & 0xFF)))

void ws2812_pixel_rgb_to_buf_dma(uint16_t * BUF_DMA, uint32_t val, uint16_t posX)
{
	uint8_t Rpixel = get_r_val(val);
	uint8_t Gpixel = get_g_val(val);
	uint8_t Bpixel = get_b_val(val);

  volatile uint16_t i;
  for(i=0;i<8;i++)
  {
    if (BitIsSet(Rpixel,(7-i)) == 1)
    {
      BUF_DMA[DELAY_LEN+posX*24+i+8] = HIGH;
    }else
    {
      BUF_DMA[DELAY_LEN+posX*24+i+8] = LOW;
    }
    if (BitIsSet(Gpixel,(7-i)) == 1)
    {
      BUF_DMA[DELAY_LEN+posX*24+i+0] = HIGH;
    }else
    {
      BUF_DMA[DELAY_LEN+posX*24+i+0] = LOW;
    }
    if (BitIsSet(Bpixel,(7-i)) == 1)
    {
      BUF_DMA[DELAY_LEN+posX*24+i+16] = HIGH;
    }else
    {
      BUF_DMA[DELAY_LEN+posX*24+i+16] = LOW;
    }
  }
}

#include "../MDK-ARM/image.h"

int sys_tick = 0;
volatile int delay = 0xEFFFFFFF;
int cur_tick = 0;
int cur_deg = 0;

#define INDICATOR_PER_DEG 2
#define IMAGE_ARR_SIZE    (360 * INDICATOR_PER_DEG) // == (sizeof(font_hex))
#define QUARTER_COUNT (IMAGE_ARR_SIZE/4) // == N of lights between each detector

void set_delay(int new_del)
{
	cur_deg = cur_deg + (QUARTER_COUNT - (cur_deg % QUARTER_COUNT));
	delay = new_del + 1;
}

void update_strip()
{
	int i = 0;
	cur_deg = (cur_deg + 1) % IMAGE_ARR_SIZE;

  int im_id_1 = (cur_deg);
  int im_id_2 = (cur_deg + QUARTER_COUNT) % IMAGE_ARR_SIZE;
  int im_id_3 = (cur_deg + QUARTER_COUNT) % IMAGE_ARR_SIZE;
  int im_id_4 = (cur_deg + QUARTER_COUNT) % IMAGE_ARR_SIZE;

	for (i = 0; i < LED_COUNT ; i++)
	{		
		ws2812_pixel_rgb_to_buf_dma(STRIP1_BUF,
																fontHEX[im_id_1][i], i);
		
		ws2812_pixel_rgb_to_buf_dma(STRIP2_BUF,
																fontHEX[im_id_2][i], i);
		
		ws2812_pixel_rgb_to_buf_dma(STRIP3_BUF,
																fontHEX[im_id_3][i], i);
		
		ws2812_pixel_rgb_to_buf_dma(STRIP4_BUF,
																fontHEX[im_id_4][i], i);
	}
}

